package basicSyntax;

public class Variables {

	public static void main(String[] args) {
		int num1 = 100;
		double num2 = 3.14;
		
		String myLike = "私の好きな";
		String thisIs = "です。";
		
		System.out.println(num1);
		System.out.println(num2);
		
		System.out.println(myLike + "整数は" + num1 + thisIs);
		System.out.println(myLike + "少数は" + num2 + thisIs);
		
	}

}
